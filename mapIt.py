#!/usr/bin/env python
# mapit.py - launches a map in the browser using an address from the cmd line or clipboard

import webbrowser
import sys
import pyperclip

if len(sys.argv) > 1:
    address = ' '.join(sys.argv[1:])
else:
    address = pyperclip.paste()

webbrowser.open('https://www.google.com/maps/place/' + address)